void criarMapa(int mapa[TAM][TAM], int fase) {
    for (int i = 0; i < TAM; i++) {
        for (int j = 0; j < TAM; j++) {
            switch (fase){
                case 1: mapa[i][j] = mapaMedio1[i][j]; break;
                case 2: mapa[i][j] = mapaMedio2[i][j]; break;
                case 3: mapa[i][j] = mapaMedio3[i][j]; break;
            }

            if (fase == 3 && mapa[i][j] == 2) {
                mapa[i][j] = 0; // Remove o portal do mapa na fase 3
            }
        }
    }
}

bool verificarPortal(int mapa[TAM][TAM], int x, int y) {
    return (mapa[x][y] == 2); // 2 representa o portal
};

void desenharMapa(bool revelaMapa[TAM][TAM], int mapa[TAM][TAM], int x, int y, int ix, int iy, int bx, int by, int projetil_x, int projetil_y, int lado) {
    for (int i = 0; i < TAM; i++) {
        for (int j = 0; j < TAM; j++) {
            if (i > x - 5 && i < x + 5 && j > y - 5 && j < y + 5) {
                revelaMapa[i][j] = true;
            };
            
            if (lado == 1 && i + 1 == x && j == y) cout<<" ▲";
            else if (lado == 2 && i - 1 == x && j == y) cout<<" ▼";
            else if (lado == 3 && i == x && j + 1 == y) cout<<" ◄";
            else if (lado == 4 && i == x && j - 1 == y) cout<<"► ";

            else if (i == x && j == y) cout << "$$"; // Jogador
            else if (i == projetil_x && j == projetil_y) cout << " ●"; // Projetil
            else if (i == bx && j == by && revelaMapa[i][j] == true) cout << "∑∑"; // Boss
            else if (i == ix && j == iy && revelaMapa[i][j] == true) cout << "%%"; // Inimigo
            else if (!revelaMapa[i][j]) cout << " -"; // Parte não revelada
            else if (mapa[i][j] == 1) cout << "██"; // Parede
            else if (mapa[i][j] == 0 && revelaMapa[i][j]) cout << "  "; // Espaço vazio revelado
            else if (mapa[i][j] == 2 && revelaMapa[i][j]) cout << "ØØ";

        }
        cout << endl;
    }
}

void resetarRevelarMapa(bool revelarMapa[TAM][TAM]) {
    for (int i = 0; i < TAM; i++) {
        for (int j = 0; j < TAM; j++) {
            revelarMapa[i][j] = false;
        }
    }
}
